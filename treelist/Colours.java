package com.technoelevate.treelist;

public class Colours {
	private int red;
	private int yellow;
	private int blue;
	private int green;
	private int voilet;
	private int orange;

	public Integer getVoilet() {
		return voilet;
	}

	public void setVoilet(int voilet) {
		this.voilet = voilet;
	}

	public Integer getOrange() {
		return orange;
	}

	public void setOrange(int orange) {
		this.orange = orange;
	}

	public Colours(int red, int yellow, int blue, int green, int voilet, int orange) {
		this.red = red;
		this.yellow = yellow;
		this.blue = blue;
		this.green = green;
		this.voilet = voilet;
		this.orange = orange;
	}

	public Integer getRed() {
		return red;
	}

	public void setRed(int red) {
		this.red = red;
	}

	public Integer getYellow() {
		return yellow;
	}

	public void setYellow(int yellow) {
		this.yellow = yellow;
	}

	public Integer getBlue() {
		return blue;
	}

	public void setBlue(int blue) {
		this.blue = blue;
	}

	public Integer getGreen() {
		return green;
	}

	public void setGreen(int green) {
		this.green = green;
	}

}
